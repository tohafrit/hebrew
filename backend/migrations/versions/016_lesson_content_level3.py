"## Условные предложения в иврите (משפטי תנאי)\n\n"
"Условные предложения — одна из ключевых конструкций на пути к свободному "
"владению ивритом. В иврите различают **три типа** условий.\n\n"
"## Тип 1: Реальное условие (תנאי ריאלי)\n\n"
"Описывает **вероятную** ситуацию. Используется **אִם** (им — если) "
"с настоящим или будущим временем.\n\n"
"| Условие (אם...) | Результат |\n"
"|---|---|\n"
"| **אִם תִּלְמַד** (им тильмад) — если ты будешь учить | **תַּצְלִיחַ** (тацлиах) — ты преуспеешь |\n"
"| **אִם יֵרֵד גֶּשֶׁם** (им йеред гéшем) — если пойдёт дождь | **נִשָּׁאֵר בַּבַּיִת** (нишаэр ба-бáйт) — мы останемся дома |\n"
"| **אִם יֵשׁ לְךָ זְמַן** (им йеш лехá зман) — если у тебя есть время | **בּוֹא** (бо) — приходи |\n\n"
"## Тип 2: Нереальное условие (תנאי לא ריאלי)\n\n"
"Описывает **маловероятную или нереальную** ситуацию. "
"Используется **אִילּוּ** (илу — если бы) или **לוּ** (лу — если бы) "
"с прошедшим временем **הָיָה** (hая — был) + причастие.\n\n"
"- **אִילּוּ הָיִיתִי עָשִׁיר, הָיִיתִי קוֹנֶה בַּיִת** "
"(илу hаити ашир, hаити конé бáйт) — если бы я был богатым, я бы купил дом\n"
"- **לוּ יָדַעְתִּי, לֹא הָיִיתִי בָּא** "
"(лу ядáти, ло hаити ба) — если бы я знал, я бы не пришёл\n\n"
"**Структура:** אילו + הייתי/היית/היה + причастие (настоящее время)\n\n"
"## Тип 3: Нереальное в прошлом\n\n"
"Описывает **то, что не произошло** в прошлом.\n\n"
"- **אִילּוּ הָיִיתִי לוֹמֵד יוֹתֵר, הָיִיתִי מַצְלִיחַ בַּמִּבְחָן** "
"(илу hаити ломéд йотéр, hаити мацлиáх ба-мивхáн) — "
"если бы я больше учился, я бы сдал экзамен\n\n"
"## Полезные союзы условия\n\n"
"| Союз | Произношение | Значение | Пример |\n"
"|---|---|---|---|\n"
"| **אִם** | им | если | אם תרצה — если захочешь |\n"
"| **אִילּוּ** | илу | если бы | אילו ידעתי — если бы я знал |\n"
"| **לוּ** | лу | если бы (поэт.) | לו יכולתי — если бы я мог |\n"
