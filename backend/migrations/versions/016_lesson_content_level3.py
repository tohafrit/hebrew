"## Страдательный залог в иврите (הסביל)\n\n"
"Пассивный залог в иврите выражается через специальные **биньяны** — "
"это не отдельная грамматическая конструкция (как в русском с «-ся»), "
"а целые глагольные модели.\n\n"
"## Пары активный — пассивный\n\n"
"| Активный биньян | Пассивный биньян | Пример (корень כ.ת.ב) |\n"
"|---|---|---|\n"
"| **פָּעַל** (Пааль) | **נִפְעַל** (Нифъаль) | כָּתַב (катав, написал) → נִכְתַּב (нихтав, был написан) |\n"
"| **פִּיעֵל** (Пиэль) | **פּוּעַל** (Пуаль) | סִיפֵּר (сипéр, рассказал) → סוּפַּר (супáр, было рассказано) |\n"
"| **הִפְעִיל** (Хифъиль) | **הוּפְעַל** (Хуфъаль) | הִזְמִין (hизмин, пригласил) → הוּזְמַן (hузман, был приглашён) |\n\n"
"## Нифъаль (נִפְעַל) — самый частый пассив\n\n"
"### Прошедшее время\n\n"
"| Лицо | Спряжение | Пример (корень כ.ת.ב) |\n"
"|---|---|---|\n"
"| אני | **נִ1*2*3תי** | נִכְתַּבְתִּי (нихтавти) — я был написан |\n"
"| אתה | **נִ1*2*3ת** | נִכְתַּבְתָּ (нихтавтá) |\n"
"| את | **נִ1*2*3ת** | נִכְתַּבְתְּ (нихтавт) |\n"
"| הוא | **נִ1*2*3** | נִכְתַּב (нихтав) |\n"
