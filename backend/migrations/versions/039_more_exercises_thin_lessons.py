exercises_table = sa.table(
    "exercises",
    sa.column("lesson_id", sa.Integer),
    sa.column("type", sa.String),
    sa.column("difficulty", sa.Integer),
    sa.column("prompt_json", sa.Text),
    sa.column("answer_json", sa.Text),
    sa.column("explanation_json", sa.Text),
    sa.column("points", sa.Integer),
)


def _j(data):
    return json.dumps(data, ensure_ascii=False)


def _mc(lesson_id, difficulty, question, text_he, options, correct, explanation, points=10):
    return {
        "lesson_id": lesson_id, "type": "multiple_choice", "difficulty": difficulty, "points": points,
        "prompt_json": _j({"question": question, "text_he": text_he, "options": options}),
        "answer_json": _j({"correct": correct}),
        "explanation_json": _j({"text": explanation}),
    }


def _fb(lesson_id, difficulty, sentence, hint, correct, alternatives, explanation, points=10):
    return {
        "lesson_id": lesson_id, "type": "fill_blank", "difficulty": difficulty, "points": points,
        "prompt_json": _j({"sentence": sentence, "hint": hint}),
        "answer_json": _j({"correct": correct, "alternatives": alternatives or []}),
        "explanation_json": _j({"text": explanation}),
    }


def _mp(lesson_id, difficulty, instruction, pairs, explanation, points=10):
    return {
        "lesson_id": lesson_id, "type": "match_pairs", "difficulty": difficulty, "points": points,
        "prompt_json": _j({"instruction": instruction, "pairs": [{"left": p[0], "right": p[1]} for p in pairs]}),
        "answer_json": _j({"pairs": [[i, i] for i in range(len(pairs))]}),
        "explanation_json": _j({"text": explanation}),
    }


def _wo(lesson_id, difficulty, words, translation, correct_order, explanation, points=15):
    return {
        "lesson_id": lesson_id, "type": "word_order", "difficulty": difficulty, "points": points,
        "prompt_json": _j({"instruction": "Расставьте слова в правильном порядке:", "words": words, "translation": translation}),
        "answer_json": _j({"correct_order": correct_order}),
        "explanation_json": _j({"text": explanation}),
    }


def _tr(lesson_id, difficulty, text_ru, hints, correct, alternatives, explanation, points=15):
    return {
        "lesson_id": lesson_id, "type": "translate_ru_he", "difficulty": difficulty, "points": points,
        "prompt_json": _j({"text_ru": text_ru, "hints": hints}),
        "answer_json": _j({"correct": correct, "alternatives": alternatives or []}),
        "explanation_json": _j({"text": explanation}),
    }


def _dc(lesson_id, difficulty, audio_text, hint, correct, explanation, points=15):
    return {
        "lesson_id": lesson_id, "type": "dictation", "difficulty": difficulty, "points": points,
        "prompt_json": _j({"audio_text": audio_text, "hint": hint}),
        "answer_json": _j({"correct": correct}),
        "explanation_json": _j({"text": explanation}),
    }


