def _j(data):
    return json.dumps(data, ensure_ascii=False)


def _mc(lid, q, opts, correct_idx, expl, diff=1):
    return {
        "lesson_id": lid, "type": "multiple_choice", "difficulty": diff, "points": 10,
        "prompt_json": _j({"question": q, "options": opts}),
        "answer_json": _j({"correct": opts[correct_idx], "correct_index": correct_idx}),
        "explanation_json": _j({"text": expl}),
    }


def _fb(lid, sent, correct, accept, ctx, diff=1):
    return {
        "lesson_id": lid, "type": "fill_blank", "difficulty": diff, "points": 10,
        "prompt_json": _j({"sentence_ru": sent, "context": ctx, "blank_position": 1}),
        "answer_json": _j({"correct": correct, "accept": accept}),
        "explanation_json": _j({"text": ctx}),
    }


def _mp(lid, left, right, pairs, expl, diff=1):
    return {
        "lesson_id": lid, "type": "match_pairs", "difficulty": diff, "points": 10,
        "prompt_json": _j({"pairs_left": left, "pairs_right": right}),
        "answer_json": _j({"correct_pairs": pairs}),
        "explanation_json": _j({"text": expl}),
    }


def _wo(lid, words, instr, translation, order, expl, diff=1):
    return {
        "lesson_id": lid, "type": "word_order", "difficulty": diff, "points": 10,
        "prompt_json": _j({"words": words, "instruction": instr, "translation": translation}),
        "answer_json": _j({"correct_order": order}),
        "explanation_json": _j({"text": expl}),
    }


def _tr(lid, sent_ru, hint, correct, accept, expl, diff=1):
    return {
        "lesson_id": lid, "type": "translate_ru_he", "difficulty": diff, "points": 10,
        "prompt_json": _j({"sentence_ru": sent_ru, "hint": hint}),
        "answer_json": _j({"correct": correct, "accept": accept}),
        "explanation_json": _j({"text": expl}),
    }


def _dc(lid, audio, instr, correct, accept, expl, diff=1):
    return {
        "lesson_id": lid, "type": "dictation", "difficulty": diff, "points": 10,
        "prompt_json": _j({"audio_text": audio, "instruction": instr}),
        "answer_json": _j({"correct": correct, "accept": accept}),
        "explanation_json": _j({"text": expl}),
    }


