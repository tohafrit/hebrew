def upgrade() -> None:
    conn = op.get_bind()

    # ── Step 1: Find all L4-L6 words that have template-generated examples ──
    # Template examples from migration 132 have transliteration=NULL and audio_url=NULL
    words = conn.execute(sa.text("""
        SELECT w.id, w.hebrew, w.translation_ru, w.pos, w.level_id
        FROM words w
        JOIN example_sentences es ON es.word_id = w.id
        WHERE w.level_id IN (4, 5, 6)
          AND es.transliteration IS NULL
          AND es.audio_url IS NULL
        ORDER BY w.id
    """)).fetchall()

    if not words:
        print("No template examples found for L4-L6 words")
        return

    print(f"Found {len(words)} L4-L6 words with template examples to replace")

    # ── Step 2: Delete old template examples ──
    conn.execute(sa.text("""
        DELETE FROM example_sentences
        WHERE transliteration IS NULL
          AND audio_url IS NULL
          AND word_id IN (
              SELECT id FROM words WHERE level_id IN (4, 5, 6)
          )
    """))
    print("Deleted old template examples")

    # ── Step 3: Insert new diverse examples ──
    sentences_table = sa.table(
        "example_sentences",
        sa.column("word_id", sa.Integer),
        sa.column("hebrew", sa.Text),
        sa.column("translation_ru", sa.Text),
        sa.column("transliteration", sa.Text),
        sa.column("audio_url", sa.String),
        sa.column("level_id", sa.Integer),
    )

    rng = random.Random(139)
    BATCH_SIZE = 500
    batch = []
    total = 0

    # Shuffle template pools so assignment isn't predictable
    noun_pool = list(NOUN_ADV)
    verb_pool = list(VERB_ADV)
    adj_pool = list(ADJ_ADV)
    generic_pool = list(GENERIC_ADV)

    for i, word_row in enumerate(words):
        word_id = word_row[0]
        hebrew = word_row[1]
        translation_ru = word_row[2]
        pos = word_row[3]
        level_id = word_row[4]

        # Select template based on POS
        if pos and pos.lower() in ("noun", "существительное", "n"):
            pool = noun_pool
        elif pos and pos.lower() in ("verb", "глагол", "v"):
            pool = verb_pool
        elif pos and pos.lower() in ("adjective", "adj", "прилагательное", "a"):
            pool = adj_pool
        else:
            pool = generic_pool

        # Rotate through pool using index
        tmpl_idx = i % len(pool)
        he_tmpl, ru_tmpl = pool[tmpl_idx]

        he_sentence = he_tmpl.format(word=hebrew)
        ru_sentence = ru_tmpl.format(tr=translation_ru)

        batch.append({
            "word_id": word_id,
            "hebrew": he_sentence,
            "translation_ru": ru_sentence,
            "transliteration": None,
            "audio_url": None,
            "level_id": level_id,
        })

        if len(batch) >= BATCH_SIZE:
            op.bulk_insert(sentences_table, batch)
            total += len(batch)
            batch = []

    if batch:
        op.bulk_insert(sentences_table, batch)
        total += len(batch)

    print(f"Inserted {total} quality example sentences for L4-L6 words")


