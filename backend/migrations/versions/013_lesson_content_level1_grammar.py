"| Неопределённое | Определённое |\n"
"|---|---|\n"
"| **יֶלֶד טוֹב** (йéлед тов) — хороший мальчик | **הַיֶּלֶד הַטּוֹב** (hа-йéлед hа-тов) — (этот) хороший мальчик |\n"
