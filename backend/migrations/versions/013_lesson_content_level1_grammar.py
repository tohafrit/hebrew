"## Определённый артикль הַ (ha-)\n\n"
"В иврите есть только **один артикль** — определённый **הַ** (ha-). "
"Неопределённого артикля (как «a/an» в английском) нет.\n\n"
"Артикль прикрепляется **к началу слова** слитно:\n"
"- **סֵפֶר** (сéфер) — книга (какая-то) → **הַסֵּפֶר** (hа-сéфер) — (эта) книга\n"
"- **יֶלֶד** (йéлед) — мальчик → **הַיֶּלֶד** (hа-йéлед) — (этот) мальчик\n"
"- **עִיר** (ир) — город → **הָעִיר** (hа-ир) — (этот) город\n\n"
"## Основное правило\n\n"
"Артикль = **הַ** + дагеш в следующей букве:\n\n"
"| Без артикля | С артиклем | Перевод |\n"
"|---|---|---|\n"
"| **בַּיִת** (бáйт) | **הַבַּיִת** (hа-бáйт) | дом → этот дом |\n"
"| **סֵפֶר** (сéфер) | **הַסֵּפֶר** (hа-сéфер) | книга → эта книга |\n"
"| **תַּלְמִיד** (тальмид) | **הַתַּלְמִיד** (hа-тальмид) | ученик → этот ученик |\n"
"| **מוֹרָה** (мора) | **הַמּוֹרָה** (hа-мора) | учительница → эта учительница |\n\n"
"## Перед гортанными буквами\n\n"
"Гортанные буквы (א, ה, ח, ע, ר) **не принимают дагеш**. "
"Поэтому артикль перед ними меняет огласовку:\n\n"
"| Буква | Артикль | Пример |\n"
"|---|---|---|\n"
"| **א, ע** | **הָ** (hа) | הָאִישׁ (hа-иш) — мужчина |\n"
"| **ח, ע** (с камац) | **הֶ** (hэ) | הֶחָכָם (hэ-хахам) — мудрец |\n"
"| **ה** | **הָ** (hа) | הָהַר (hа-hар) — гора |\n"
"| **ר** | **הָ** (hа) | הָרֹאשׁ (hа-рош) — голова |\n\n"
"На практике начинающим достаточно запомнить: перед гортанными — **הָ** (долгое «а»), "
"остальные нюансы придут с опытом.\n\n"
"## Артикль с предлогами\n\n"
"Предлоги **בְּ** (в), **לְ** (к, для), **כְּ** (как) сливаются с артиклем:\n\n"
"| Предлог + артикль | Результат | Пример |\n"
"|---|---|---|\n"
"| **בְּ + הַ** | **בַּ** (ба-) | **בַּבַּיִת** (ба-бáйт) — в доме |\n"
"| **לְ + הַ** | **לַ** (ла-) | **לַיֶּלֶד** (ла-йéлед) — мальчику |\n"
"| **כְּ + הַ** | **כַּ** (ка-) | **כַּמּוֹרֶה** (ка-морé) — как учитель |\n\n"
"**Буква ה артикля исчезает**, а предлог получает патах и дагеш в следующей букве.\n\n"
"## Когда ставить артикль?\n\n"
"- **Конкретный** предмет: Дай мне **הַסֵּפֶר** (ту книгу)\n"
"- **Уже упомянутый**: Вчера видел собаку. **הַכֶּלֶב** был большой\n"
"- **Единственный в своём роде**: **הַשֶּׁמֶשׁ** (солнце), **הַיָּרֵחַ** (луна)\n"
"- **С прилагательным** (обязательно на обоих!): **הַיֶּלֶד הַטּוֹב** (хороший мальчик)\n\n"
"## Артикль и прилагательные\n\n"
"Это ключевое правило: если существительное определённое, "
