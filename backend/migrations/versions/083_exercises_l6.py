_j = lambda d: json.dumps(d, ensure_ascii=False)


def _mc(lid, diff, q, opts, ci, exp):
    return {"lesson_id": lid, "type": "multiple_choice", "difficulty": diff, "points": 10,
            "prompt_json": _j({"question": q, "options": opts}),
            "answer_json": _j({"correct": opts[ci], "correct_index": ci}),
            "explanation_json": _j({"text": exp})}


def _fb(lid, diff, sent, hint, cor, alt, exp):
    return {"lesson_id": lid, "type": "fill_blank", "difficulty": diff, "points": 10,
            "prompt_json": _j({"sentence": sent, "hint": hint}),
            "answer_json": _j({"correct": cor, "alternatives": alt or []}),
            "explanation_json": _j({"text": exp})}


def _mp(lid, diff, instr, pairs, exp):
    return {"lesson_id": lid, "type": "match_pairs", "difficulty": diff, "points": 10,
            "prompt_json": _j({"instruction": instr, "pairs": [{"left": p[0], "right": p[1]} for p in pairs]}),
            "answer_json": _j({"pairs": [[i, i] for i in range(len(pairs))]}),
            "explanation_json": _j({"text": exp})}


def _wo(lid, diff, words, trans, order, exp):
    return {"lesson_id": lid, "type": "word_order", "difficulty": diff, "points": 10,
            "prompt_json": _j({"instruction": "Расставьте слова в правильном порядке:", "words": words, "translation": trans}),
            "answer_json": _j({"correct_order": order}),
            "explanation_json": _j({"text": exp})}


def _tr(lid, diff, ru, hints, cor, alt, exp):
    return {"lesson_id": lid, "type": "translate_ru_he", "difficulty": diff, "points": 10,
            "prompt_json": _j({"text_ru": ru, "hints": hints}),
            "answer_json": _j({"correct": cor, "alternatives": alt or []}),
            "explanation_json": _j({"text": exp})}


def _dc(lid, diff, audio, hint, cor, exp):
    return {"lesson_id": lid, "type": "dictation", "difficulty": diff, "points": 10,
            "prompt_json": _j({"audio_text": audio, "hint": hint}),
            "answer_json": _j({"correct": cor}),
            "explanation_json": _j({"text": exp})}


