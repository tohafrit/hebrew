exercises_table = sa.table(
    "exercises",
    sa.column("lesson_id", sa.Integer),
    sa.column("type", sa.String),
    sa.column("difficulty", sa.Integer),
    sa.column("prompt_json", sa.Text),
    sa.column("answer_json", sa.Text),
    sa.column("explanation_json", sa.Text),
    sa.column("points", sa.Integer),
)

def _j(d):
    return json.dumps(d, ensure_ascii=False)

def _mc(lid, q, opts, ci, expl, d=1):
    return {"lesson_id":lid,"type":"multiple_choice","difficulty":d,"points":10,
            "prompt_json":_j({"question":q,"options":opts}),
            "answer_json":_j({"correct":opts[ci],"correct_index":ci}),
            "explanation_json":_j({"text":expl})}

def _fb(lid, sent, hint, correct, alts, expl, d=1):
    return {"lesson_id":lid,"type":"fill_blank","difficulty":d,"points":10,
            "prompt_json":_j({"sentence":sent,"hint":hint}),
            "answer_json":_j({"correct":correct,"alternatives":alts or []}),
            "explanation_json":_j({"text":expl})}

def _mp(lid, instr, pairs, expl, d=1):
    return {"lesson_id":lid,"type":"match_pairs","difficulty":d,"points":10,
            "prompt_json":_j({"instruction":instr,"pairs":[{"left":p[0],"right":p[1]} for p in pairs]}),
            "answer_json":_j({"pairs":[[i,i] for i in range(len(pairs))]}),
            "explanation_json":_j({"text":expl})}

def _wo(lid, words, translation, order, expl, d=1):
    return {"lesson_id":lid,"type":"word_order","difficulty":d,"points":10,
            "prompt_json":_j({"instruction":"Расставьте слова в правильном порядке:","words":words,"translation":translation}),
            "answer_json":_j({"correct_order":order}),
            "explanation_json":_j({"text":expl})}

def _tr(lid, text_ru, hints, correct, alts, expl, d=1):
    return {"lesson_id":lid,"type":"translate_ru_he","difficulty":d,"points":10,
            "prompt_json":_j({"text_ru":text_ru,"hints":hints}),
            "answer_json":_j({"correct":correct,"alternatives":alts or []}),
            "explanation_json":_j({"text":expl})}

def _dc(lid, audio_text, hint, correct, expl, d=1):
    return {"lesson_id":lid,"type":"dictation","difficulty":d,"points":10,
            "prompt_json":_j({"audio_text":audio_text,"hint":hint}),
            "answer_json":_j({"correct":correct}),
            "explanation_json":_j({"text":expl})}

