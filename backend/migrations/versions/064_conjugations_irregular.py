def _past(wid, bid, forms):
    persons = [("1","","s"),("2","m","s"),("2","f","s"),("3","m","s"),("3","f","s"),("1","","p"),("2","m","p"),("2","f","p"),("3","","p")]
    return [{"word_id":wid,"binyan_id":bid,"tense":"past","person":p,"gender":g,"number":n,"form_he":f[0],"form_nikkud":None,"transliteration":f[1]} for (p,g,n),f in zip(persons, forms)]

def _present(wid, bid, forms):
    persons = [("m","s"),("f","s"),("m","p"),("f","p")]
    return [{"word_id":wid,"binyan_id":bid,"tense":"present","person":"","gender":g,"number":n,"form_he":f[0],"form_nikkud":None,"transliteration":f[1]} for (g,n),f in zip(persons, forms)]

def _future(wid, bid, forms):
    persons = [("1","","s"),("2","m","s"),("2","f","s"),("3","m","s"),("3","f","s"),("1","","p"),("2","m","p"),("2","f","p"),("3","m","p"),("3","f","p")]
    return [{"word_id":wid,"binyan_id":bid,"tense":"future","person":p,"gender":g,"number":n,"form_he":f[0],"form_nikkud":None,"transliteration":f[1]} for (p,g,n),f in zip(persons, forms)]

def _imperative(wid, bid, forms):
    persons = [("m","s"),("f","s"),("m","p"),("f","p")]
    return [{"word_id":wid,"binyan_id":bid,"tense":"imperative","person":"2","gender":g,"number":n,"form_he":f[0],"form_nikkud":None,"transliteration":f[1]} for (g,n),f in zip(persons, forms)]

def _verb(wid, bid, past, pres, fut, imp):
    return _past(wid,bid,past)+_present(wid,bid,pres)+_future(wid,bid,fut)+_imperative(wid,bid,imp)

