def _get_templates(pos, level_id):
    """Select appropriate templates based on POS and level."""
    level = level_id or 1

    if pos and pos.lower() in ("noun", "существительное", "שם עצם"):
        if level <= 2:
            return NOUN_SIMPLE
        elif level <= 4:
            return NOUN_INTERMEDIATE
        else:
            return NOUN_ADVANCED
    elif pos and pos.lower() in ("verb", "глагол", "פועל"):
        if level <= 2:
            return VERB_SIMPLE
        elif level <= 4:
            return VERB_INTERMEDIATE
        else:
            return VERB_ADVANCED
    elif pos and pos.lower() in ("adjective", "adj", "прилагательное", "שם תואר"):
        if level <= 2:
            return ADJ_SIMPLE
        elif level <= 4:
            return ADJ_INTERMEDIATE
        else:
            return ADJ_ADVANCED
    else:
        if level <= 2:
            return OTHER_SIMPLE
        elif level <= 4:
            return OTHER_INTERMEDIATE
        else:
            return OTHER_ADVANCED


