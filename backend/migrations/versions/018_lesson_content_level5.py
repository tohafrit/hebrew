"## Инфинитивные конструкции на практике (מבני שם פועל: תרגול)\n\n"
"Инфинитив (שֵׁם הַפּוֹעַל) в иврите — невероятно гибкий инструмент. На уровне C1 "
"вы должны уметь использовать инфинитив не только в базовых конструкциях "
"(«хочу + инфинитив»), но и в сложных сочетаниях с предлогами, в абсолютном "
"инфинитиве и в конструкциях, характерных для формального стиля.\n\n"
"---\n\n"
"## 1. Инфинитив с предлогами (שם פועל עם מילות יחס)\n\n"
"### Предлог **-בְּ** (бе-) + инфинитив = «во время, при»\n\n"
"Конструкция **-בְּ** + инфинитив указывает на **одновременность** действий:\n\n"
"| Конструкция | Транслитерация | Перевод |\n"
"|---|---|---|\n"
"| **בְּהַגִּיעוֹ הַבַּיְתָה** | бе-hагиó hа-бáйта | по прибытии его домой |\n"
"| **בְּשָׁמְעוֹ אֶת הַחֲדָשׁוֹת** | бе-шомъо эт hа-хадашот | услышав новости |\n"
"| **בְּצֵאתָם מִן הַבִּנְיָן** | бе-цетам мин hа-биньян | при выходе их из здания |\n"
"| **בִּרְאוֹתִי אוֹתוֹ** | бир-оти ото | увидев его |\n\n"
"**Важно:** к инфинитиву присоединяются **притяжательные суффиксы** "
"(-י, -ו, -ה, -נו, -כם, -ם), указывающие на субъект действия.\n\n"
"**Примеры в контексте:**\n"
"- **בְּהַגִּיעוֹ לָעִיר, הוּא הִתְקַשֵּׁר לָאָבִיו** (бе-hагиó ла-ир, hу "
"hиткашер ле-авив) — Прибыв в город, он позвонил отцу\n"
"- **בְּשָׁמְעָהּ אֶת הַבְּשׂוֹרָה, הִתְפָּרְצָה בְּבֶכִי** (бе-шомъа эт "
"hа-бсора, hитпарца бе-вехи) — Услышав весть, она разрыдалась\n\n"
"### Предлог **-לְ** (ле-) + инфинитив = цель\n\n"
"Это самая базовая конструкция, но на C1 она усложняется:\n\n"
"- **כְּדֵי לְהַבְטִיחַ** (кдей леhавтиах) — чтобы обеспечить\n"
"- **עַל מְנָת לְ-** (аль менат ле-) — с тем чтобы (формальное)\n"
"- **בְּמַטָּרָה לְ-** (бе-матара ле-) — с целью (формальное)\n"
"- **לְשֵׁם** (лешем) + существительное — ради, во имя\n\n"
"Примеры:\n"
"- **עַל מְנָת לְשַׁפֵּר אֶת הַמַּצָּב** (аль менат лешапер эт hа-мацав) — "
"С тем чтобы улучшить ситуацию\n"
"- **בְּמַטָּרָה לְקַדֵּם אֶת הַפְּרוֹיֶקְט** (бе-матара лекадем эт hа-проект) — "
"С целью продвижения проекта\n\n"
"### Предлог **-מִ** (ми-) + инфинитив = «от» / причина\n\n"
"| Конструкция | Транслитерация | Перевод |\n"
"|---|---|---|\n"
"| **מֵרוֹב לֶאֱכוֹל** | мэ-ров лээхоль | от чрезмерной еды |\n"
"| **מִלִּשְׁמוֹעַ** | ми-лишмóа | от слушания |\n"
"| **מִלִּרְאוֹת** | ми-лиръот | от виденного |\n\n"
"- **הוּא עָיֵף מִלִּשְׁמוֹעַ תַּלְקוּנוֹת** (hу аеф ми-лишмóа тлунот) — "
"Он устал слушать жалобы\n"
"- **נִמְאַס לִי מִלְּחַכּוֹת** (нимас ли ме-лехакот) — "
"Мне надоело ждать\n\n"
"---\n\n"
"## 2. Абсолютный инфинитив (מקור מוחלט)\n\n"
"Абсолютный инфинитив — архаичная, но живая конструкция. Он используется "
"для **усиления** глагола и встречается в формальном, юридическом и "
"литературном стиле.\n\n"
"**Структура:** абсолютный инфинитив + спрягаемый глагол того же корня.\n\n"
"| Абсолютный инфинитив | Пример | Транслитерация | Перевод |\n"
"|---|---|---|---|\n"
"| **הָלוֹךְ** | **הָלוֹךְ הָלַכְנוּ** | hалох hалáхну | мы шли и шли |\n"
"| **שָׁמוֹר** | **שָׁמוֹר תִּשְׁמוֹר** | шамор тишмор | непременно сохрани |\n"
"| **זָכוֹר** | **זָכוֹר תִּזְכּוֹר** | захор тизкор | непременно помни |\n"
"| **אָכוֹל** | **אָכוֹל תֹּאכַל** | ахоль тохаль | непременно ешь |\n\n"
"**В современном языке** абсолютный инфинитив используется как наречие:\n"
"- **הוּא הָלַךְ הָלוֹךְ וְגָדֵל** (hу hалах hалох ве-гадéль) — "
"Он всё рос и рос\n"
"- **הַמַּצָּב הוֹלֵךְ וּמִשְׁתַּפֵּר** (hа-мацав hолех у-миштапéр) — "
"Ситуация постепенно улучшается\n"
"- **הַמְּחִירִים הוֹלְכִים וְעוֹלִים** (hа-мехирим hольхим ве-олим) — "
"Цены всё повышаются\n\n"
"---\n\n"
"## 3. Сложные инфинитивные конструкции\n\n"
"### Инфинитив как подлежащее\n\n"
"В формальном иврите инфинитив может быть **подлежащим** предложения:\n\n"
"- **לִלְמוֹד עִבְרִית זֶה כֵּיף** (лильмод иврит зэ кейф) — Учить иврит — это удовольствие\n"
"- **לִנְסוֹעַ בָּרָכֶבֶת זוֹל יוֹתֵר** (линсóа ба-ракéвет золь йотер) — "
"Ездить на поезде дешевле\n"
"- **לִכְתּוֹב חִיבּוּר אֲקָדֶמִי דּוֹרֵשׁ תִּרְגּוּל** (лихтов хибур акадéми "
"дореш тиргуль) — Написание академической работы требует практики\n\n"
"### Цепочки инфинитивов\n\n"
"На C1 вы часто встретите **цепочки** инфинитивов:\n\n"
"- **הוּא נֶאֱלַץ לְהַפְסִיק לִלְמוֹד כְּדֵי לַעֲבוֹד** (hу неэлац леhафсик "
"лильмод кдей лаавод) — Он был вынужден прекратить учиться, чтобы работать\n"
"- **הִיא הִצְלִיחָה לְהַתְחִיל לְהָבִין** (hи hицлиха леhатхиль леhавин) — "
"Ей удалось начать понимать\n\n"
"---\n\n"
"## 4. Упражнения\n\n"
"Переведите на иврит, используя инфинитивные конструкции:\n\n"
