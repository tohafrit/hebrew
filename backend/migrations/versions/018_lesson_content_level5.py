"## Нифъаль и Хитпаэль: оттенки значения (נפעל והתפעל: גוונים)\n\n"
"На начальных уровнях вы узнали, что Нифъаль (נִפְעַל) — «пассивный», а "
"Хитпаэль (הִתְפַּעֵל) — «возвратный» биньян. На уровне C1 эта картина "
"становится значительно сложнее. Оба биньяна обладают **множеством "
"семантических оттенков**, и понимание этих нюансов — ключ к подлинно "
"глубокому владению ивритом.\n\n"
"---\n\n"
"## 1. Нифъаль (נפעל): четыре значения\n\n"
"### А. Пассив (סביל)\n\n"
"Самое известное значение — действие, совершённое **над** субъектом:\n\n"
"| Пааль (активный) | Нифъаль (пассивный) | Перевод |\n"
"|---|---|---|\n"
"| **כָּתַב** (катав) — написал | **נִכְתַּב** (нихтав) — был написан | Письмо **было написано** вчера |\n"
"| **שָׁבַר** (шавар) — сломал | **נִשְׁבַּר** (нишбар) — сломался/был сломан | Стакан **разбился** |\n"
"| **פָּתַח** (патах) — открыл | **נִפְתַּח** (нифтах) — был открыт/открылся | Дверь **открылась** |\n\n"
"### Б. Рефлексив (החזרי) — действие направлено на самого субъекта\n\n"
"- **נִשְׁמַר** (нишмар) — остерёгся, поберёгся (от שָׁמַר — охранял)\n"
"- **נֶעֱלַם** (неэлам) — исчез (от עָלַם — скрыл)\n"
"- **נֶעֱצַר** (неэцар) — остановился; был задержан (от עָצַר — остановил)\n\n"
"### В. Реципрок (הדדי) — взаимное действие\n\n"
"- **נִפְגַּשׁ** (нифгаш) — встретился (с кем-то) (от פָּגַשׁ — встретил)\n"
"- **נִלְחַם** (нильхам) — сражался (от לָחַם — воевал)\n"
"- **נֶאֱבַק** (нээвак) — боролся\n\n"
"### Г. Статив (מצבי) — состояние\n\n"
"- **נִשְׁאַר** (нишъар) — остался (состояние «оставшегося»)\n"
"- **נִרְאֶה** (ниръэ) — выглядит, кажется\n"
"- **נִדְמֶה** (нидмэ) — кажется\n"
"- **נֶחְשַׁב** (нехшав) — считается\n\n"
"---\n\n"
"## 2. Хитпаэль (התפעל): пять значений\n\n"
"### А. Рефлексив (החזרי) — действие на себя\n\n"
"| Пиэль (активный) | Хитпаэль (рефлексив) | Перевод |\n"
"|---|---|---|\n"
"| **לָבַשׁ** (лаваш) — одел (кого-то) | **הִתְלַבֵּשׁ** (hитлабеш) — оделся | Он **оделся** |\n"
"| **רָחַץ** (рахац) — помыл | **הִתְרַחֵץ** (hитрахец) — помылся | Она **помылась** |\n"
"| **גִּלַּח** (гилéах) — побрил | **הִתְגַּלֵּחַ** (hитгалéах) — побрился | Он **побрился** |\n\n"
"### Б. Реципрок (הדדי) — взаимное действие\n\n"
