"### 7. Когда и почему израильтяне переключают регистры\n\n"
"**Факторы переключения:**\n\n"
"- **Собеседник** (בן שיח): с начальником — формальнее, с другом — разговорнее\n"
"- **Тема** (נושא): политика, право — формальнее; быт, развлечения — разговорнее\n"
"- **Канал** (ערוץ): письменная речь — формальнее; устная — разговорнее\n"
"- **Эмоции** (רגשות): при волнении — переход к разговорному; при аргументации — к формальному\n"
"- **Статус** (מעמד): с незнакомыми — формальнее; со знакомыми — разговорнее\n"
"- **Юмор** (הומור): ироничное использование формального регистра в неформальной ситуации\n\n"
