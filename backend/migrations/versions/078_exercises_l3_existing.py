def _j(data):
    return json.dumps(data, ensure_ascii=False)


def _mc(lid, diff, question, options, correct_idx, explanation):
    return {
        "lesson_id": lid, "type": "multiple_choice", "difficulty": diff, "points": 10,
        "prompt_json": _j({"question": question, "options": options}),
        "answer_json": _j({"correct": options[correct_idx], "correct_index": correct_idx}),
        "explanation_json": _j({"text": explanation}),
    }


def _fb(lid, diff, sentence, hint, correct, accept, explanation):
    return {
        "lesson_id": lid, "type": "fill_blank", "difficulty": diff, "points": 10,
        "prompt_json": _j({"sentence": sentence, "hint": hint}),
        "answer_json": _j({"correct": correct, "alternatives": accept or []}),
        "explanation_json": _j({"text": explanation}),
    }


def _mp(lid, diff, instruction, pairs, explanation):
    return {
        "lesson_id": lid, "type": "match_pairs", "difficulty": diff, "points": 10,
        "prompt_json": _j({"instruction": instruction, "pairs": [{"left": p[0], "right": p[1]} for p in pairs]}),
        "answer_json": _j({"pairs": [[i, i] for i in range(len(pairs))]}),
        "explanation_json": _j({"text": explanation}),
    }


def _wo(lid, diff, words, translation, correct_order, explanation):
    return {
        "lesson_id": lid, "type": "word_order", "difficulty": diff, "points": 10,
        "prompt_json": _j({"instruction": "Расставьте слова в правильном порядке:", "words": words, "translation": translation}),
        "answer_json": _j({"correct_order": correct_order}),
        "explanation_json": _j({"text": explanation}),
    }


def _tr(lid, diff, text_ru, hints, correct, alternatives, explanation):
    return {
        "lesson_id": lid, "type": "translate_ru_he", "difficulty": diff, "points": 10,
        "prompt_json": _j({"text_ru": text_ru, "hints": hints}),
        "answer_json": _j({"correct": correct, "alternatives": alternatives or []}),
        "explanation_json": _j({"text": explanation}),
    }


def _dc(lid, diff, audio_text, hint, correct, explanation):
    return {
        "lesson_id": lid, "type": "dictation", "difficulty": diff, "points": 10,
        "prompt_json": _j({"audio_text": audio_text, "hint": hint}),
        "answer_json": _j({"correct": correct}),
        "explanation_json": _j({"text": explanation}),
    }


