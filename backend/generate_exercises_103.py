EXERCISES = []

def ex(lesson_id, typ, prompt, answer, explanation, diff=2, pts=10):
    EXERCISES.append((lesson_id, typ, diff, json.dumps(prompt, ensure_ascii=False),
                      json.dumps(answer, ensure_ascii=False),
                      json.dumps(explanation, ensure_ascii=False), pts))

def fb(lid, sentence, correct, hint, expl, alts=None):
    p = {"sentence": sentence, "hint": hint}
    a = {"correct": correct}
    if alts: a["alternatives"] = alts
    ex(lid, "fill_blank", p, a, {"text": expl})

def mc(lid, question, options, correct_idx, expl):
    p = {"question": question, "options": options}
    a = {"correct": options[correct_idx], "correct_index": correct_idx}
    ex(lid, "multiple_choice", p, a, {"text": expl})

def mp(lid, left, right, matches):
    p = {"pairs_left": left, "pairs_right": right}
    a = {"matches": matches}
    ex(lid, "match_pairs", p, a, {"text": "Соедините пары правильно."})

def wo(lid, words, correct, translation):
    p = {"words_shuffled": words, "correct_sentence": correct, "translation": translation}
    a = {"correct_order": correct.split()}
    ex(lid, "word_order", p, a, {"text": f"Правильный порядок: {correct}"})

def tr(lid, ru, he, expl):
    p = {"sentence_ru": ru}
    a = {"correct": he}
    ex(lid, "translate_ru_he", p, a, {"text": expl})

