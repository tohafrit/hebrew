def build_dialogue_rows(dialogues):
    lines = []
    for i, d in enumerate(dialogues):
        lines.append(f"    # {i+1}. {d['title']}")
        lines.append("    {")
        lines.append(f'        "level_id": {d["level_id"]},')
        lines.append(f'        "title": {json.dumps(d["title"], ensure_ascii=False)},')
        lines.append(f'        "situation_ru": {json.dumps(d["situation_ru"], ensure_ascii=False)},')
        lines.append(f'        "lines_json": _j({json.dumps(json.loads(d["lines_json"]), ensure_ascii=False)}),')
        lines.append(f'        "vocabulary_json": _j({json.dumps(json.loads(d["vocabulary_json"]), ensure_ascii=False)}),')
        lines.append(f'        "audio_url": None,')
        lines.append("    },")
    return "\n".join(lines)


